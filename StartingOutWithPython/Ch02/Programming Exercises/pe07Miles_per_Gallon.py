# Miles per gallon, mpg
# MPG = Miles driven Gallons of fas used
# Miles driven over gallons of gas used 

miles = (float(input('Enter the the number of miles you have driven: ')))

gas = (float(input('Enter the how many gallons of gas that you have used: ')))

MPG = miles / gas

print('Your cars travels', MPG, \
      ' miles per gallon of gas')